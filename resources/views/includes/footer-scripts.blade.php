    $(".submitCoupe").click(function() {
        var id = $(this).attr("data-id");
        var coup = $('#coupon' + id).val();
        var update = $(this).data("update");
        if (coup != '') {
            $("#messagealert").hide();
            $.ajax({
                type: "post",
                url: "{{route('coupon')}}",
                data: {
                    '_token': "{{csrf_token()}}",
                    id: id,
                    coup: coup,
                    update: update
                },
                success: function(data) {
                    if (data == 1) {
                        if (update == 'update') {
                            $(".coupUpdate" + id).text(coup);
                            toastr.success('Success!', 'Coupon Updated Successfully!');
                        } else {
                            toastr.success('Success!', 'Coupon Added Successfully!');
                            $(".row" + id).fadeOut();
                        }
                    }
                }
            });
        } else {
            $("#messagealert").show();
            $("#flashmessage").text('Please! Enter Coupon.');
        }


    });

